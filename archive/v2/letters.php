<h1>/letters</h1>

<p>Leaving for Vietnam, February 2009</p>

<p>So, I was working on improved cook stoves all last semester for rural Nicaragua (see: [1] ) Along the way I met people from countries all over the world. One of those guys was a retired recycling engineer living in Da Lat, Vietnam, who was building exactly the kind of stove I was working with (rice husk gasification) in order to get rid of waste rice husk / make cooking safer. He invited me to come help him a few months ago. I didn't know what to do at first, but as the semester went on, I couldn't stop thinking about it. The prospect of a new language, design for the developing world, microeconomic solutions to real problems, entrepreneurial training: it was very alluring.</p>

<p>There's this interesting computational aspect to it all, too; the challenge to this sort of work is often in finding and organizing design information as well as managing online collaboration from many countries/cultures. There are opportunities for collecting good data and doing good science. Policy-makers are itching for statistically significant results to help guide their efforts in coming years.</p>

<p>I decided to help Paul with his work in Vietnam.</p>

<p>I found out that this work with appropriate technology is infectious. Our senior design group tested cook stoves outside at Georgia Tech for an entire semester. People walking by loved talking about fire and would poke their heads over the reactor (even if it threatened singed eyebrows). We taught Engineers Without Borders how to make <em>tincanium</em> stoves, and they brought the designs to Cameroon. This semester, they've already had dozens of undergraduate engineers volunteer to tinker with their own stoves. There are many groups like this around the world, but they are not yet connected very well. It is going to be exciting when online community managers and Open Source ethos make their way into the group. (hint: we're already scheming)</p>

<p>Something important to me is that this be a real, humble effort (in contrast to an overblown, imaginary one). For better or for worse, university curriculums stick to the abstract. Interpreted the wrong way, this leads to students that skim by with ideas that are never tested against reality. I was no exception (example: thinking that because a problem was complicated made it interesting). Muhammad Yunus said the same about his time teaching in the States, calling it a "caricature". There are people at Georgia Tech trying to change that, and I really hope that they succeed. And, of course, there are people there already doing it right. I do still get excited by reading Buckminster Fuller, thinking that I can be an "anticipatory design scientist" and affect the course of civilization through technology. What I am really excited about, though, is getting to add more cuts to my hands in the quest to fifty and finally making it as a real stove designer.</p> 

<p>I am not sure where this will lead. Ostensibly, this trip is to make use of my time before entering graduate school. Paul, though, says that I won't be able to bring myself to leave Vietnam. The graduate programs I applied for will be a good intellectual follow-up to the issues that the work will bring up, but opportunities may arise between now and then that glimmer with even more of the magic that has drawn me to Vietnam.</p>

