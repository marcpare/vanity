<p>I'll try to make it something Claude L&eacute;vi-Strauss would be happy to receive (i.e. not "le menu tant de d&eacute;tails inspides, d'&eacute;v&eacute;nements inginfiants"..."a blow-by-blow full of insipid details and insignificant events"). If you have anything you want me looking for while I'm out there, let me know.</p>

